#include <iostream>
using namespace std;

int main() {
    for (int i = 5; i <= 7; i++) {
       cout << i << endl;
    }

    cout << endl;

    for (int i = 5; i < 7; i++) {
       cout << i << endl;
    }

    cout << endl;

    for (int i = 0; i <= 10; i += 2) {
       cout << i << endl;
    }

    cout << endl;
    
    for (int i = 10; i >= 0; i--) {
       cout << i << endl;
    }

    return 0;
}
